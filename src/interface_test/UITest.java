    private void btn_kayit_olActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_kayit_olActionPerformed
        String f_name = tf_name.getText();
        String l_name = tf_surname.getText();
        String mail = tf_mail.getText();
        String password = String.valueOf(pf_password.getPassword());
        String phone = tf_tel.getText();
        
        if(f_name.isEmpty() | l_name.isEmpty() | mail.isEmpty() | password.isEmpty() | phone.isEmpty())
        {
            lbl_message.setForeground(Color.red);
            lbl_message.setText("Alanlar boş bırakılmamalıdır.");
        }
        
        else
        {
            try 
            {
                if(db.isUniqueMail(mail))
                {
                    db.addUser(f_name, l_name, mail, password, phone);
                    lbl_message.setForeground(Color.green);
                    lbl_message.setText("Başarıyla kayıt oldunuz.");
                    this.tf_name.setText("");
                    this.tf_surname.setText("");
                    this.tf_mail.setText("");
                    this.pf_password.setText("");
                    this.tf_tel.setText("");
                }
                
                else
                {
                    lbl_message.setForeground(Color.red);
                    lbl_message.setText("Girilen e-posta adresi zaten kayıtlı!");
                }
            } 
            
            catch (SQLException ex) 
            {
                Logger.getLogger(UITest.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_kayit_olActionPerformed

    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        String mail = tf_mail_login.getText();
        String password = String.valueOf(pf_login.getPassword());
        try 
        {
            if(mail.isEmpty() || password.isEmpty())
            {
                lbl_login_msg.setForeground(Color.red);
                lbl_login_msg.setText("Alanların hepsi doldurulmalıdır.");
            }
            
            else
            {
                if(db.isRegistered(mail, password))
                {
                    this.isLoggedIn = true;
                    btn_sign_in.setVisible(false);
                    btn_register.setVisible(false);
                    setLoggedIn();
                    hidePanels(search_books_pane);
                    btn_favorites.setVisible(false);
                    btn_mail.setVisible(false);
                    btn_sign_out.setVisible(false);
                    btn_add_favorite.setVisible(true);
                    this.loggedMail = mail;
                }
                
                else
                {
                    lbl_login_msg.setForeground(Color.red);
                    lbl_login_msg.setText("Girilen bilgilere sahip bir kullanıcı bulunamadı.");
                }
            }
        } 
        
        catch (SQLException ex) 
        {
            Logger.getLogger(UITest.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_loginActionPerformed

    private void btn_add_favoriteMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_add_favoriteMousePressed
        
        if(model == null || model.getRowCount() == 0)
        {
            lbl_table_msg.setForeground(Color.red);
            lbl_table_msg.setText("Önce bir arama yapmalısınız.");
        }

        else
        {
            int index = book_table.getSelectedRow();
            if(index == -1)
            {
                lbl_table_msg.setForeground(Color.red);
                lbl_table_msg.setText("Önce bir kitabın üstüne tıklamalısınız."); 
            }
            
            else
            {
                float last_price = (Float)book_table.getValueAt(index, 2);
                float shipment_and_price = (Float)book_table.getValueAt(index, 4);
                String seller = book_table.getValueAt(index, 3).toString();
                String url = book_table.getValueAt(index, 5).toString();
                String text = book_table.getValueAt(index, 1).toString();
                System.out.println(last_price);

                try 
                {
                    boolean isAdded = db.addFavorite(loggedMail, text, last_price, shipment_and_price, seller, url);
                    if(isAdded)
                    {
                        ImageIcon filled_star = new ImageIcon("src/interface_test/images/icons8_star_filled_20px.png");
                        filled_star.getImage().flush();
                        btn_add_favorite.setIcon(filled_star);
                        lbl_table_msg.setForeground(Color.green);
                        lbl_table_msg.setText("Kitap başarıyla favorilerinize eklendi.");
                    }

                    else
                    {
                        lbl_table_msg.setForeground(Color.red);
                        lbl_table_msg.setText("Kitap zaten favorilerinizde ekli.");
                    }
                }

                catch (SQLException ex) 
                {
                    ex.toString();
                    Logger.getLogger(UITest.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btn_add_favoriteMousePressed

