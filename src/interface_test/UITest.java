    public void showBooks(String search) throws IOException
    {
        k = new KitapIslemleri();
        Parser.getBooks().clear();
        Parser[] sellers = {new TestSitesi(search), new KitapYurdu(search), new DR(search), new BKM(search), new Amazon(search), new Idefix(search)};
        k.addBooks(sellers);
        
        model = (DefaultTableModel)book_table.getModel();
        model.setRowCount(0);
        ImageIcon icon;
        JLabel img_label;
        
        for(ArrayList book: Parser.getBooks())
        {
            icon = new ImageIcon(new URL(String.valueOf(book.get(2))));
            img_label = new JLabel();
            Image img = icon.getImage().getScaledInstance(60, 60, Image.SCALE_SMOOTH);
            img_label.setIcon(new ImageIcon(img));
            float with_shipment = Float.valueOf(book.get(4).toString()) + Float.valueOf(book.get(6).toString());
            book_url = book.get(1).toString();
            Object[] eklenecek = {img_label, book.get(0), book.get(4), book.get(5), with_shipment, book_url};
            model.addRow(eklenecek);
        }
        
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(model);
        book_table.setRowSorter(tr);
        ArrayList<RowSorter.SortKey> sortKeys = new ArrayList<>();
        sortKeys.add(new RowSorter.SortKey(4, SortOrder.ASCENDING));
        tr.setSortKeys(sortKeys);
        tr.sort();
    }
    
    public void showFavorites() throws SQLException
    {
        model = (DefaultTableModel)favorites_table.getModel();
        model.setRowCount(0);
        ArrayList<ArrayList<String>> favorites = db.getFavoriteInfos(loggedMail);
        if(favorites != null)
        {
            for(ArrayList<String> book: favorites)
            {
                Object[] eklenecek = {book.get(0), book.get(1), book.get(2), book.get(3), book.get(4)};
                model.addRow(eklenecek);
            }
        }
    }

