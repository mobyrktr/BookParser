    void resetColors(JPanel button)
    {
        for(JPanel btn: this.buttons)
        {
            if(!btn.equals(button))
            {
                this.resetColor(btn);
            }
        }
    }
    
    void hidePanels(JPanel panel)
    {
        for(JPanel pane: this.panels)
        {
            if(!pane.equals(panel))
            {
                pane.setVisible(false);
            }
        }
    }
    
